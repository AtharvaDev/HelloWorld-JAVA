
public class Add2Num {

	public static void main(String[] args) {
//		Data Types
//		use anything small caps alpha, numbers and underscore.
//		start with alpha or underscore
//		cannot start with numbers eg. 1a, 8icbas
		
		
//		short: it will take 2 bytes
		short s = 123;
		

//		int: it will take 4 bytes
		int i = 10;
		int j = 15;  
		
		int k = i + j;
		System.out.println(k);
		
		
//		long: it will take 8 bytes
		long l = 1234; 
		
		
//		For decimals
//		1. Double: 4 bytes
//		2. Float 8 bytes
		double d = 1.23;
		float f = 24.5f;
		
		
//		For Characters: 2 bytes
		char c = 'a';
		
		
//		Boolean: 1 bit
		Boolean one = false;

	}

}
